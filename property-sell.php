Prepare your property for the market with the following steps:

Fix any broken external elements such as fencing or guttering
Re-decorate brightly coloured rooms in favour of a more neutral tone
De-clutter each room and make maximising space a priority
Be careful to remove all traces of cigarette smoke or pet smells and remnants
Replace any broken internal elements such as light bulbs or cracked tiles
